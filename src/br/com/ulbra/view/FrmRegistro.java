    private void btnBuscarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarRegistroActionPerformed
        PessoaDAO dao = new PessoaDAO();
        Pessoa p = new Pessoa();
        
        p = dao.findById(Integer.parseInt(edtId.getText()));
        
        edtNome.setText(p.getNome());
        edtDataNasc.setText(p.getDataNasc());
        edtEmail.setText(p.getEmail());
        edtCelular.setText(p.getCelular());
        edtNumero.setText(String.valueOf(p.getNumero()));
        edtCep.setText(p.getCep());
        
        if(p.getSexo().equals("Masculino")){
            radioMasculino.setSelected(true);
        }else{
            radioFeminino.setSelected(true);
        }
 
        txtBairro.setText("");
        txtCidade.setText("");
        txtLogradouro.setText("");
        txtUf.setText("");
        
    }//GEN-LAST:event_btnBuscarRegistroActionPerformed

