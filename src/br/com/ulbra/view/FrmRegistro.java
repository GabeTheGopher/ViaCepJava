    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        PessoaDAO dao = new PessoaDAO();
        Pessoa p = new Pessoa();
        String sexo;
        
        if(radioMasculino.isSelected()){
            sexo = "Masculino";
        }else{
            sexo = "Feminino";
        }
        
        p.setId(Integer.parseInt(edtId.getText()));
        p.setNome(edtNome.getText());
        p.setCep(edtCep.getText());
        p.setDataNasc(edtDataNasc.getText());
        p.setEmail(edtEmail.getText());
        p.setNumero(Integer.parseInt(edtNumero.getText()));
        p.setSexo(sexo);
        p.setCelular(edtCelular.getText());
        
        dao.save(p);
    }//GEN-LAST:event_btnEditarActionPerformed

